=begin
1オブジェクト指向の目的
    →オブジェクトは物という訳になる通り様々なプログラムを細分化し一つ一つのアクションとして捉えている。
    そのアクションを使用するために指示（要素・データ）を入れ、そのアクションを様々利用すること。
2カプセル化
    →上記の通りアクション（オブジェクト）を使用する際に指示（要素・データ）が異なるとアクションが正常に利用できなくなるため、
    そのアクション（オブジェクト）と指示（要素・データ）を外部から変更できないようにすること。
3継承
    →アクション（オブジェクト）と指示（要素・データ）をより細かいアクションにすること。特化させること。
4ポリモーフィズム
    →アクション（オブジェクト）の細分化（継承）で細分化先がそれぞれ異なる挙動をすることをポリモーフィズムという。
    これらはこれらは例えるなら、『泳ぐ』というアクション（オブジェクト）で継承され細分化されたアクションをそれぞれ、『平泳ぎ』『クロール』に分けると、
    大分類のアクションは同じだが、細分化した時の挙動はそれぞれ異なっている。
5単一責任の原則
    →オブジェクトやクラスなどに対して原則として一つの機能にしか責任を持たないというもの。
    仕様変更などの際に、複数機能を担うクラスなどがあれば一つの変更に対して全ての機能をテストする必要が出る。
=end