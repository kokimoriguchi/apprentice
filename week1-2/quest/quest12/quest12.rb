#1モジュールについて
    ・モジュールとは何か
        →関数やメソッドを定義しクラスなどに入れ込むことができる。クラスの様だが、機能の共通点のみを共有することができるオブジェクト。
    ・クラスの違いは何か
        →クラスは同じ機能を持つものは継承で対応できるが、そのクラスが＝で繋がる存在でない場合は修正があった場合継承されたクラス全てを修正することになる可能性が高い。
        そこで、＝で繋がらないクラスだが一部同じ機能があると言う場合にモジュールはその機能を各クラスに入れ込むことができる。
    ・モジュールを使うメリットは何か
        →様々なクラスや機能を持つコードの際に、重複した機能があるがクラスとしては共通ではないオブジェクトに共通機能のみを共有することができるようになる。
        また、Moduleクラスのの継承先にClassクラスがあるためClass名の重複をModuleのクラスを分けることで区別することが可能。
